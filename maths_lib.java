
public class maths_lib {
    public static void main(String[] args){
        //for maximum value
        System.out.println("MAXIMUM NUMBER IS : "+Math.max(1,9));
        //for minimum number
        System.out.println("MINIMUM NUMBER IS : "+Math.min(1,9));
        //for random number between 0.0 to 1.0
        System.out.println("RANDOM FLOAT NUMBER IS : "+Math.random());
        //for random number between1 to 9
        System.out.println("REAL NUMBER IS : "+(int)(100*Math.random()));
    }
}
